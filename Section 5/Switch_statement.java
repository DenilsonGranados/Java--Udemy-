public class Switch_statement {
    public static void main(String[] args){
        int swaitchValue=1;
        switch(swaitchValue){
            case 1:
            System.out.println("Value was 1");
            break;

            case 2:
            System.out.println("Value was 2");
            break;

            default:
            System.out.println("Was not 1 or 2");
            break;
        }
    }
}
